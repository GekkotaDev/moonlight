import type { StarlightSidebarTopicsUserOptions } from "starlight-sidebar-topics";
import type { StarlightIcon } from "@astrojs/starlight/types";

import starlightSidebarTopics from "starlight-sidebar-topics";

type Topic = {
  label: string;
  icon: StarlightIcon;
  link: string;
  items: (Autogenerated | Section | Page<string>)[];

  id?: string | undefined;
};

type Autogenerated = {
  autogenerate: { directory: string };
};

type Section = {
  label: string;
  items: (Autogenerated | Section | Page<string>)[];
};

type Page<T extends string> = {
  slug: Slug<T>;
};

type Slug<T extends string> = T extends `/${string}/`
  ? never
  : T extends `${string}/`
    ? never
    : T extends `/${string}`
      ? never
      : T;

